//right sfx--->prompt sfx
function testcorrect() {
  let which = Math.floor(Math.random() * 3);
  var right = new Howl({
    src: rightSound[which],
    autoplay: true,
    loop: false,
    volume: 0.6,
    onend: function () {
      request();
    },
  });
  right.play();
}

