function playletter() {
  for (
    i = 0;
    i < objectsInWorld.length;
    i++ // if the wrong letter/number/shape was clicked say the letter then play a wrong sfx
  )
    if (mouseConstraint.body == objectsInWorld[i]) {
      var wrongbody = soundsInWorld[i];
    }
  var touched = new Howl({
    src: wrongbody,
    autoplay: true,
    loop: false,
    volume: 0.6,
    onend: function () {
      testwrong();
    },
  });
  touched.play();
}
